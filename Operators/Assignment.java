package Operators;

public class Assignment {
    public static void main(String[] args) {
        int a = 10; // = is the assignment operator
        System.out.println(a += 5); // this means a = a + 5
        System.out.println(a -= 5); // this means a = a - 5
        System.out.println(a *= 5); // this means a = a * 5
        System.out.println(a /= 5); // this means a = a / 5
        System.out.println(a %= 5); // this means a = a % 5
    }
}


// NOTE: AFTER EVERY OPERATION THE VALUE OF A IS GETTING CHANGED!